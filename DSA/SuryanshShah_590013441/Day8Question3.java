import java.util.*;
class Day8Question3
{
    public static void main(String[] args)
    {
        String str;
        int l;
        System.out.println("Enter a String");
        Scanner scr=new Scanner(System.in);
        str=scr.nextline();
        l=str.length();
        for(int i=l;i>0;i--)
        {
            System.out.print(str.charAt[i]);
        }
    }
}
