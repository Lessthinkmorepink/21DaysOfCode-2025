#Fixed Input
string = "Hello World"
length = len(string)
upper = string.upper()
lower = string.lower()
print("Length : ", length,", Original : ", string,", Uppercase : ",upper, ", Lowercase : ", lower)

#Custom Input
string = input("Enter a string")
length = len(string)
upper = string.upper()
lower = string.lower()
print("Length : ", length,", Original : ", string,", Uppercase : ",upper, ", Lowercase : ", lower)
